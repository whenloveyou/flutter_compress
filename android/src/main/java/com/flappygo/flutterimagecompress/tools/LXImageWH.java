package com.flappygo.flutterimagecompress.tools;

public class LXImageWH {
	private int height;
	private int width;
	public LXImageWH() {
	}
	public LXImageWH(int width, int height) {
		this.width = width;
		this.height = height;
	}

	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}

}
